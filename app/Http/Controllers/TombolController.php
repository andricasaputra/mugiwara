     */
    public function update(Request $request)
    {
        $tombols = Tombol::all();
        foreach ($tombols as $key => $value) {
           $status = $value->status;
        }

        if($status == 'on') {
            Tombol::where('status', 'on')->update(['status' => 'off']);
        }

        if($status == 'off') {
            Tombol::where('status', 'off')->update(['status' => 'on']);
        }

        return redirect()->route('admin.mitraGabung.mitraGabung');
    }

